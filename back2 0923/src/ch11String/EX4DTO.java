package ch11String;

public class EX4DTO {
	
	int no;
	int date;
	int coment;
	public EX4DTO() {}
	public EX4DTO(int no, int date, int coment) {
		super();
		this.no = no;
		this.date = date;
		this.coment = coment;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getDate() {
		return date;
	}
	public void setDate(int date) {
		this.date = date;
	}
	public int getComent() {
		return coment;
	}
	public void setComent(int coment) {
		this.coment = coment;
	}
	
	
	
	
}
